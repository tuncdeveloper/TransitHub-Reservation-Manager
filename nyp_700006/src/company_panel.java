    private void deleteVehicleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteVehicleBtnActionPerformed
        ReservationSystem reSystem=new ReservationSystem();
        CompanyVehicleTableForm model= (CompanyVehicleTableForm) vehicleTable.getModel();
       
        //Company company = new Company(newCompanyName.getText(), newCompanyPassword.getText(), newCompanyHizmetBedeli.getText());
        //reSystem.getCompanyList().add(company);
        for(Vehicle vehicle:reSystem.getVehicleList()){
            if(vehicle.getVehicleNo().equals(newVehicleNoInputText.getText())){
                reSystem.getVehicleList().remove(vehicle);
                break;
            }
        }
        vehicleTable.setModel(new CompanyVehicleTableForm(reSystem.getVehicleList()));
        jScrollPane1.setViewportView(vehicleTable);
        model.addTableModelListener(vehicleTable); 
    }//GEN-LAST:event_deleteVehicleBtnActionPerformed


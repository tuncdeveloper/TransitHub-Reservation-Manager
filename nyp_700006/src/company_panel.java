    private List<Vehicle>getVehicleList(ReservationSystem reservationSystem){
        
        Iterator<Vehicle> iterator = reservationSystem.getVehicleList().iterator();
        while (iterator.hasNext()) {
            Vehicle vehicle = iterator.next();

            // Silme koşulunu burada belirtin (örneğin, "Car" olanları silme)
            if (!vehicle.getCompanyName().equals(company_login_panel.loginCompanyName)) {
                iterator.remove(); // Iterator üzerinden silme işlemi
            }
        }
        return reservationSystem.getVehicleList();
    }
    
        private List<Personel>getPersonelList(ReservationSystem reservationSystem){
        
        Iterator<Personel> iterator = reservationSystem.getPersonelList().iterator();
        while (iterator.hasNext()) {
            Personel personel = iterator.next();

            // Silme koşulunu burada belirtin (örneğin, "Car" olanları silme)
            if (!personel.getCompanyName().equals(company_login_panel.loginCompanyName)) {
                iterator.remove(); // Iterator üzerinden silme işlemi
            }
        }
        return reservationSystem.getPersonelList();
    }

